import { initializeApp, getApps, FirebaseApp } from 'firebase/app';
import { getDatabase, ref, onValue, set, remove, push, Database } from 'firebase/database';

/**
 * Firebase Service - Komunikacja Camera <-> Remote przez Firebase Realtime Database
 *
 * Architektura:
 * - Kamera: Nas≈Çuchuje na /signals/{sessionId}/
 * - Pilot: Wysy≈Ça sygna≈Çy do /signals/{sessionId}/
 * - Session ID: Generowany przez kamerƒô, udostƒôpniany przez QR lub kod
 */

export interface P2PMessage {
    type: 'register' | 'capture' | 'ping' | 'status';
    role?: 'camera' | 'remote';
    timestamp?: number;
    duration?: number;
}

type MessageHandler = (message: P2PMessage) => void;
type ConnectionHandler = (connected: boolean) => void;

class FirebaseService {
    private app: FirebaseApp | null = null;
    private database: Database | null = null;
    private messageHandlers: MessageHandler[] = [];
    private connectionHandlers: ConnectionHandler[] = [];
    private deviceRole: 'camera' | 'remote' | null = null;
    private sessionId: string = '';
    private unsubscribeSignals: (() => void) | null = null;
    private unsubscribeStatus: (() => void) | null = null;

    /**
     * Inicjalizuj Firebase
     * UWAGA: Musisz najpierw skonfigurowaƒá Firebase w konsoli
     */
    initialize(firebaseConfig: any) {
        try {
            // Sprawd≈∫ czy Firebase ju≈º jest zainicjalizowany
            if (getApps().length === 0) {
                this.app = initializeApp(firebaseConfig);
                console.log('üî• Firebase initialized');
            } else {
                this.app = getApps()[0];
                console.log('üî• Firebase already initialized');
            }

            this.database = getDatabase(this.app);
            console.log('üìä Database ready');
        } catch (error) {
            console.error('‚ùå Firebase initialization failed:', error);
            throw error;
        }
    }

    /**
     * Ustaw rolƒô urzƒÖdzenia
     */
    setDeviceRole(role: 'camera' | 'remote') {
        this.deviceRole = role;
        console.log(`üì± Device role: ${role}`);
    }

    /**
     * KAMERA: Utw√≥rz sesjƒô i zacznij nas≈Çuchiwaƒá sygna≈Ç√≥w
     */
    async startAsCamera(): Promise<string> {
        if (!this.database) {
            throw new Error('Firebase not initialized');
        }

        this.deviceRole = 'camera';

        // Generuj unikalny session ID (6 znak√≥w, ≈Çatwy do wpisania)
        this.sessionId = this.generateSessionId();

        console.log('üìπ Camera mode started');
        console.log(`üîë Session ID: ${this.sessionId}`);

        // Ustaw status kamery jako online
        const statusRef = ref(this.database, `sessions/${this.sessionId}/camera/status`);
        await set(statusRef, {
            online: true,
            timestamp: Date.now(),
            role: 'camera',
        });

        // Nas≈Çuchuj na sygna≈Çy od pilot√≥w
        const signalsRef = ref(this.database, `sessions/${this.sessionId}/signals`);

        this.unsubscribeSignals = onValue(signalsRef, (snapshot) => {
            const signals = snapshot.val();

            if (signals) {
                // Przetw√≥rz ka≈ºdy nowy sygna≈Ç
                Object.keys(signals).forEach(async (key) => {
                    const signal = signals[key];

                    console.log('üì® Received signal:', signal.type);

                    // Powiadom handlery
                    this.notifyHandlers(signal);

                    // Usu≈Ñ przetworzony sygna≈Ç
                    const signalRef = ref(this.database!, `sessions/${this.sessionId}/signals/${key}`);
                    await remove(signalRef);
                });
            }
        });

        // Nas≈Çuchuj na status pilot√≥w
        const remotesRef = ref(this.database, `sessions/${this.sessionId}/remotes`);

        this.unsubscribeStatus = onValue(remotesRef, (snapshot) => {
            const remotes = snapshot.val();
            const connected = remotes && Object.keys(remotes).length > 0;

            if (connected) {
                console.log('‚úÖ Remote(s) connected:', Object.keys(remotes).length);
            } else {
                console.log('‚è≥ Waiting for remotes...');
            }

            this.notifyConnectionHandlers(connected);
        });

        return this.sessionId;
    }

    /**
     * PILOT: Po≈ÇƒÖcz siƒô z sesjƒÖ kamery
     */
    async connectToCamera(sessionId: string): Promise<boolean> {
        if (!this.database) {
            throw new Error('Firebase not initialized');
        }

        this.deviceRole = 'remote';
        this.sessionId = sessionId.toUpperCase();

        console.log(`üéÆ Connecting to session: ${this.sessionId}`);

        try {
            // Sprawd≈∫ czy sesja istnieje
            const sessionRef = ref(this.database, `sessions/${this.sessionId}/camera/status`);

            return new Promise((resolve, reject) => {
                const timeout = setTimeout(() => {
                    reject(new Error('Session not found (timeout)'));
                }, 10000);

                onValue(sessionRef, async (snapshot) => {
                    clearTimeout(timeout);

                    const cameraStatus = snapshot.val();

                    if (cameraStatus && cameraStatus.online) {
                        console.log('‚úÖ Camera found and online');

                        // Zarejestruj pilota
                        const remoteId = Math.random().toString(36).substring(7);
                        const remoteRef = ref(this.database!, `sessions/${this.sessionId}/remotes/${remoteId}`);

                        await set(remoteRef, {
                            online: true,
                            timestamp: Date.now(),
                            role: 'remote',
                        });

                        // Nas≈Çuchuj na status kamery
                        this.unsubscribeStatus = onValue(sessionRef, (cameraSnapshot) => {
                            const status = cameraSnapshot.val();
                            const connected = status && status.online;
                            this.notifyConnectionHandlers(connected);
                        });

                        console.log('‚úÖ Connected to camera');
                        this.notifyConnectionHandlers(true);
                        resolve(true);
                    } else {
                        reject(new Error('Camera not found or offline'));
                    }
                }, { onlyOnce: true });
            });

        } catch (error) {
            console.error('‚ùå Connection failed:', error);
            throw error;
        }
    }

    /**
     * Wy≈õlij wiadomo≈õƒá
     */
    async sendMessage(message: P2PMessage): Promise<void> {
        if (!this.database || !this.sessionId) {
            console.warn('‚ö†Ô∏è Firebase not ready or no session');
            return;
        }

        try {
            // Wy≈õlij sygna≈Ç do Firebase
            const signalsRef = ref(this.database, `sessions/${this.sessionId}/signals`);
            const newSignalRef = push(signalsRef);

            await set(newSignalRef, {
                ...message,
                timestamp: Date.now(),
                role: this.deviceRole,
            });

            console.log('üì§ Sent:', message.type);
        } catch (error) {
            console.error('Failed to send message:', error);
            throw error;
        }
    }

    /**
     * Generuj 6-znakowy session ID
     */
    private generateSessionId(): string {
        const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
        let result = '';
        for (let i = 0; i < 6; i++) {
            result += chars.charAt(Math.floor(Math.random() * chars.length));
        }
        return result;
    }

    /**
     * Zarejestruj handler wiadomo≈õci
     */
    onMessage(handler: MessageHandler): () => void {
        this.messageHandlers.push(handler);
        return () => {
            this.messageHandlers = this.messageHandlers.filter(h => h !== handler);
        };
    }

    /**
     * Zarejestruj handler po≈ÇƒÖczenia
     */
    onConnection(handler: ConnectionHandler): () => void {
        this.connectionHandlers.push(handler);
        return () => {
            this.connectionHandlers = this.connectionHandlers.filter(h => h !== handler);
        };
    }

    /**
     * Powiadom handlery o wiadomo≈õci
     */
    private notifyHandlers(message: P2PMessage) {
        this.messageHandlers.forEach(handler => {
            try {
                handler(message);
            } catch (error) {
                console.error('Message handler error:', error);
            }
        });
    }

    /**
     * Powiadom handlery o po≈ÇƒÖczeniu
     */
    private notifyConnectionHandlers(connected: boolean) {
        this.connectionHandlers.forEach(handler => {
            try {
                handler(connected);
            } catch (error) {
                console.error('Connection handler error:', error);
            }
        });
    }

    /**
     * Roz≈ÇƒÖcz i wyczy≈õƒá
     */
    async disconnect(): Promise<void> {
        if (this.unsubscribeSignals) {
            this.unsubscribeSignals();
            this.unsubscribeSignals = null;
        }

        if (this.unsubscribeStatus) {
            this.unsubscribeStatus();
            this.unsubscribeStatus = null;
        }

        // Usu≈Ñ status z Firebase
        if (this.database && this.sessionId) {
            if (this.deviceRole === 'camera') {
                const sessionRef = ref(this.database, `sessions/${this.sessionId}`);
                await remove(sessionRef);
                console.log('üóëÔ∏è Session cleaned up');
            } else if (this.deviceRole === 'remote') {
                // Pilot: usu≈Ñ tylko sw√≥j status
                console.log('üëã Remote disconnected');
            }
        }

        this.deviceRole = null;
        this.sessionId = '';
        this.messageHandlers = [];
        this.connectionHandlers = [];

        console.log('üëã Firebase Service disconnected');
    }

    /**
     * Sprawd≈∫ czy jest po≈ÇƒÖczenie
     */
    isConnected(): boolean {
        return this.sessionId !== '';
    }

    /**
     * Pobierz session ID
     */
    getSessionId(): string {
        return this.sessionId;
    }

    /**
     * Pobierz rolƒô urzƒÖdzenia
     */
    getRole(): 'camera' | 'remote' | null {
        return this.deviceRole;
    }
}

export default new FirebaseService();